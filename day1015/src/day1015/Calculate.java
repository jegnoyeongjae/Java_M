package day1015;

public class Calculate {
	
	long add(long a, long b) {
		long result = a+b;
		
		return	result;
	}
	
	long minuse(long a, long b) {
		long result = a-b;
		
		return	result;
		
	}
	long subtract(long a, long b) {
		long result = a/b;
		
		return	result;
		
	}
	long mutiply(long a, long b) {
		long result = a*b;
		
		return	result;
		
	}
}
